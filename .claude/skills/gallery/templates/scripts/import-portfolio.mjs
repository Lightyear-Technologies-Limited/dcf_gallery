#!/usr/bin/env node
/**
 * Import portfolio.xlsx / .csv into src/lib/data.ts.
 *
 * Expected columns (case-insensitive):
 *   Artist name, Artist wallet, Collection name, Contract address,
 *   Token id, Title, Year, Mint date, Mint platform, Original URI,
 *   OpenSea URL, Medium (image | video | interactive | physical)
 *
 * Slug generation:
 *   Artist:     kebab-case of Artist name
 *   Collection: kebab-case of Collection name
 *   Piece:      {collectionSlug}-{tokenId}  (no hex tail — {col, tokenId}
 *               is unique by definition)
 *
 * Set the input file path via --input path/to/portfolio.xlsx (or .csv).
 * The script OVERWRITES data.ts entirely — anything hand-edited in the
 * generated arrays is lost. Editorial content (curator notes, display
 * order, hide flags) belongs in curation.json + content/editorial/,
 * NOT here.
 */
import { readFile, writeFile } from "node:fs/promises";
import { dirname, resolve, extname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");

const inputArg = process.argv.find((a) => a.startsWith("--input="))?.split("=")[1];
if (!inputArg) {
  console.error("✗ --input=<path> required (portfolio.xlsx or .csv)");
  process.exit(1);
}

const ext = extname(inputArg).toLowerCase();
let rows = [];
if (ext === ".csv") {
  const text = await readFile(inputArg, "utf-8");
  const lines = text.split(/\r?\n/).filter(Boolean);
  const headers = lines[0].split(",").map((h) => h.trim().toLowerCase());
  for (const line of lines.slice(1)) {
    // Naive CSV split — swap in a real parser if fields contain commas
    const cells = line.split(",");
    const row = {};
    headers.forEach((h, i) => (row[h] = cells[i]?.trim() ?? ""));
    rows.push(row);
  }
} else if (ext === ".xlsx" || ext === ".xls") {
  const XLSX = (await import("xlsx")).default;
  const wb = XLSX.readFile(inputArg);
  const sheet = wb.Sheets[wb.SheetNames[0]];
  rows = XLSX.utils.sheet_to_json(sheet).map((r) =>
    Object.fromEntries(Object.entries(r).map(([k, v]) => [String(k).toLowerCase(), v])),
  );
} else {
  console.error(`✗ Unsupported file type: ${ext}`);
  process.exit(1);
}

const kebab = (s) => String(s).trim().toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");

const artistsMap = new Map();
const collectionsMap = new Map();
const piecesMap = new Map();

for (const r of rows) {
  const artistName = r["artist name"] || r["artist"];
  const collectionName = r["collection name"] || r["collection"];
  const contract = String(r["contract address"] || "").toLowerCase();
  const tokenId = String(r["token id"] ?? "");
  const title = r["title"] || `${collectionName} ${tokenId}`;

  if (!artistName || !collectionName) continue;

  const artistSlug = kebab(artistName);
  const collectionSlug = kebab(collectionName);
  const pieceSlug = tokenId ? `${collectionSlug}-${tokenId}` : `${collectionSlug}-${kebab(title)}`;

  if (!artistsMap.has(artistSlug)) {
    artistsMap.set(artistSlug, {
      slug: artistSlug, name: artistName,
      wallet: r["artist wallet"] || undefined,
    });
  }
  if (!collectionsMap.has(collectionSlug)) {
    collectionsMap.set(collectionSlug, {
      slug: collectionSlug, name: collectionName, artistSlug,
      contractAddress: contract || undefined,
    });
  }
  if (piecesMap.has(pieceSlug)) continue;
  piecesMap.set(pieceSlug, {
    id: pieceSlug, slug: pieceSlug, title,
    collectionSlug, artistSlug,
    medium: r["medium"] || "image",
    contractAddress: contract || undefined,
    tokenId: tokenId || undefined,
    mintDate: r["mint date"] || undefined,
    mintPlatform: r["mint platform"] || undefined,
    year: r["year"] ? String(r["year"]) : undefined,
    openseaUrl: r["opensea url"] || undefined,
    originalUri: r["original uri"] || undefined,
  });
}

const artists = [...artistsMap.values()];
const collections = [...collectionsMap.values()];
const pieces = [...piecesMap.values()];

const stringify = (arr) => JSON.stringify(arr, null, 2).replace(/"([^"]+)":/g, "$1:");

const out = `/**
 * On-chain source layer. Generated by scripts/import-portfolio.mjs.
 * Do NOT hand-edit — anything editorial belongs in curation.json or
 * content/editorial/. See reference/ARCHITECTURE.md.
 */

export interface Artist { slug: string; name: string; bio?: string; portrait?: string; wallet?: string; tags?: string[]; }
export interface Collection { slug: string; name: string; artistSlug: string; description?: string; contractAddress?: string; totalSupply?: number; mintDate?: string; platform?: string; curatorNote?: string; artistStatement?: string; exhibitions?: { date: string; title: string; location?: string; url?: string; pieces?: string[]; }[]; }
export interface Piece { id: string; slug: string; title: string; collectionSlug: string; artistSlug: string; image?: string; medium: "image" | "video" | "interactive" | "physical"; description?: string; traits?: Record<string, string | number | string[]>; tokenId?: string; contractAddress?: string; mintDate?: string; mintPlatform?: string; year?: string; openseaUrl?: string; originalUri?: string; }

export const artists: Artist[] = ${stringify(artists)};

export const collections: Collection[] = ${stringify(collections)};

export const pieces: Piece[] = ${stringify(pieces)};

export const getArtist = (slug: string) => artists.find((a) => a.slug === slug);
export const getCollection = (slug: string) => collections.find((c) => c.slug === slug);
export const getPiece = (slug: string) => pieces.find((p) => p.slug === slug);
export const getCollectionsByArtist = (artistSlug: string) => collections.filter((c) => c.artistSlug === artistSlug);
export const getPiecesByCollection = (collectionSlug: string) => pieces.filter((p) => p.collectionSlug === collectionSlug);
export const getPiecesByArtist = (artistSlug: string) => pieces.filter((p) => p.artistSlug === artistSlug);
`;

await writeFile(resolve(ROOT, "src/lib/data.ts"), out);
console.log(`✓ data.ts — ${artists.length} artists, ${collections.length} collections, ${pieces.length} pieces`);
